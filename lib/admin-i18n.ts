export type AdminLocale = 'ko' | 'vi'

export const DEFAULT_ADMIN_LOCALE: AdminLocale = 'vi'

const ADMIN_LOCALE_STORAGE_KEY = 'admin_locale'

export function getStoredAdminLocale(): AdminLocale | null {
  if (typeof window === 'undefined') return null
  const v = localStorage.getItem(ADMIN_LOCALE_STORAGE_KEY)
  if (v === 'ko' || v === 'vi') return v
  return null
}

export function setStoredAdminLocale(locale: AdminLocale): void {
  if (typeof window === 'undefined') return
  localStorage.setItem(ADMIN_LOCALE_STORAGE_KEY, locale)
}

